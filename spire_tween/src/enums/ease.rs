impl Ease {
    pub fn sample(&self, x: f64) -> f64 {
        const A: f64 = 20.0;
        const B: f64 = 10.0;
        const C: f64 = 2.0;
        const D: f64 = 1.99804687;
        const J: f64 = 0.0004887581;

        match self {
            Ease::Default => unsafe {
                let ease = DEFAULT_EASE;
                ease.sample(x)
            },
            Ease::Linear => keyframe::functions::Linear.y(x),
            Ease::InSine => keyframe::functions::EaseIn.y(x),
            Ease::InQuad => keyframe::functions::EaseInQuad.y(x),
            Ease::InCubic => keyframe::functions::EaseInCubic.y(x),
            Ease::InQuart => keyframe::functions::EaseInQuart.y(x),
            Ease::InQuint => keyframe::functions::EaseInQuint.y(x),
            Ease::InCirc => {
                let sq_target = 1. - x * x;
                if sq_target < 0. { if x > 1. { 1. } else { 0. } } else { 1. - f64::sqrt(sq_target) }
            }
            //Ease::InExpo => f64::powf(2., 10. * x - 10.) - 0.000976562,
            // 2 (((C^(10 x-B))/(D))-J)
            Ease::InExpo => 2. * ((f64::powf(C, B * x - B) / D) - J),
            Ease::OutSine => keyframe::functions::EaseOut.y(x),
            Ease::OutQuad => keyframe::functions::EaseOutQuad.y(x),
            Ease::OutCubic => keyframe::functions::EaseOutCubic.y(x),
            Ease::OutQuart => keyframe::functions::EaseOutQuart.y(x),
            Ease::OutQuint => keyframe::functions::EaseOutQuint.y(x),
            Ease::OutCirc => {
                let x_minus_1 = x - 1.;
                let sq_target = 1. - x_minus_1 * x_minus_1;
                if sq_target < 0. { if x < 0. { 0. } else { 2. } } else { f64::sqrt(sq_target) }
            }
            // Ease::OutExpo => 1. - f64::powf(2., -10. * x),
            // 1+2 (J+((-C^(-B x))/(D)))
            Ease::OutExpo => 1. + 2. * (J - (f64::powf(C, -B * x) / D)),
            Ease::InOutSine => keyframe::functions::EaseInOut.y(x),
            Ease::InOutQuad => keyframe::functions::EaseInOutQuad.y(x),
            Ease::InOutCubic => keyframe::functions::EaseInOutCubic.y(x),
            Ease::InOutQuart => keyframe::functions::EaseInOutQuart.y(x),
            Ease::InOutQuint => keyframe::functions::EaseInOutQuint.y(x),
            Ease::InOutCirc => {
                if x < 0.5 {
                    let sq_target = 1. - 4. * x * x;

                    if sq_target < 0. { if x <= -0.5 { -0.5 } else { 0.5 } } else { (1. - f64::sqrt(sq_target)) / 2. }
                } else {
                    let w = 2. - 2. * x;
                    let sq_target = 1. - w * w;
                    if sq_target < 0. { if x >= 1.5 { 1.5 } else { 0.5 } } else { (f64::sqrt(sq_target) + 1.) / 2. }
                }
            }
            Ease::InOutExpo => {
                if x <= 0.5 {
                    (f64::powf(C, A * x - B) / D) - J
                } else {
                    1. + J - f64::powf(C, B - A * x) / D
                }
            }
            Ease::Punch => {
                // f(x)=a sin(x^(2) π f) ℯ^(-d x)
                const A: f64 = 5.79580821;
                const F: f64 = 8.;
                const D: f64 = -8.;

                A * f64::sin(x * x * PI * F) * f64::exp(D * x)
            }
        }
    }
}

#[derive(Clone)]
pub enum EaseKind {
    Basic(Ease),
    GodotCurve(Gd<Curve>),
    Callable(Callable),
    Custom(Rc<dyn EasingFunction>),
}

impl GodotConvert for EaseKind {
    type Via = Variant;
}

impl FromGodot for EaseKind {
    fn try_from_godot(via: Self::Via) -> Result<Self, ConvertError> {
        if let Ok(curve) = via.clone().try_to::<Gd<Curve>>() {
            Ok(EaseKind::GodotCurve(curve))
        } else if let Ok(callable) = via.clone().try_to::<Callable>() {
            Ok(EaseKind::Callable(callable))
        } else if let Ok(basic) = via.try_to_relaxed::<Ease>() {
            Ok(EaseKind::Basic(basic))
        } else {
            Err(ConvertError::with_error_value("Could not convert Variant to `EaseKind`", via))
        }
    }
}

impl ToGodot for EaseKind {
    type ToVia<'v> = Self::Via;

    fn to_godot(&self) -> Self::ToVia<'_> {
        match self {
            EaseKind::Basic(basic) => basic.to_variant(),
            EaseKind::GodotCurve(curve) => curve.to_variant(),
            EaseKind::Callable(callable) => callable.to_variant(),
            EaseKind::Custom(f) => {
                let f = Rc::clone(f);
                Callable::from_local_fn("anonymous_easing_fn", move |args| {
                    let arg = args.first().ok_or(())?;
                    let x = arg.try_to_relaxed::<f64>().log_if_err().unwrap_or_default();
                    Ok(f.y(x).to_variant())
                })
                .to_variant()
            }
        }
    }
}

impl Default for EaseKind {
    fn default() -> Self { Self::Basic(Default::default()) }
}

impl EaseKind {
    pub fn sample(&self, x: f64) -> f64 {
        match self {
            EaseKind::Basic(basic) => basic.sample(x),
            EaseKind::GodotCurve(curve) => curve.sample_baked(x as f32) as f64,
            EaseKind::Custom(custom) => custom.y(x),
            EaseKind::Callable(callable) => {
                let result = callable.call(&[x.to_variant()]);
                match result.try_to_relaxed::<f64>() {
                    Ok(variant) => variant,
                    Err(err) => {
                        godot_error!("Error calling easing function: {err:?}");
                        x
                    }
                }
            }
        }
    }
}

impl Debug for EaseKind {
    fn fmt(&self, f: &mut Formatter<'_>) -> std::fmt::Result {
        match self {
            EaseKind::Basic(basic) => <Ease as Debug>::fmt(basic, f),
            EaseKind::GodotCurve(curve) => f.debug_tuple("GodotCurve").field(curve).finish(),
            EaseKind::Custom(_) => f.debug_tuple("Custom").field(&"..anonymous dyn object..").finish(),
            EaseKind::Callable(callable) => f.debug_tuple("Callable").field(callable).finish(),
        }
    }
}
